class CircularQueue
  def initialize(length)
    @length = length
    @buffer = Array.new
  end

  def enqueue(item)
    dequeue if full?
    buffer.push(item)
  end

  def dequeue
    buffer.shift
  end

  private

  attr_accessor :buffer
  attr_reader :length

  def full?
    buffer.size == length
  end
end

queue = CircularQueue.new(3)
puts queue.dequeue.nil?

queue.enqueue(1)
queue.enqueue(2)
puts queue.dequeue == 1

queue.enqueue(3)
queue.enqueue(4)
puts queue.dequeue == 2

queue.enqueue(5)
queue.enqueue(6)
queue.enqueue(7)
puts queue.dequeue == 5
puts queue.dequeue == 6
puts queue.dequeue == 7
puts queue.dequeue.nil?

